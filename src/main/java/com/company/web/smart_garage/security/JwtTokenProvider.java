package com.company.web.smart_garage.security;

import com.company.web.smart_garage.exceptions.APIException;
import io.jsonwebtoken.*;
import io.jsonwebtoken.io.Decoders;
import io.jsonwebtoken.security.Keys;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.http.HttpStatus;
import org.springframework.security.core.Authentication;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;

import static com.company.web.smart_garage.utils.Constants.*;

@Component
public class JwtTokenProvider {

    @Value("${app.jwt-secret}")
    private String jwtSecret;
    @Value("${app.jwt-expiration-milliseconds}")
    private long jwtExpirationDate;

    public String generateToken(Authentication authentication) {
        String username = authentication.getName();
        Date currentDate = new Date();
        Date expireDate = new Date(currentDate.getTime() + jwtExpirationDate);

        String token = Jwts.builder()
                .setSubject(username)
                .setIssuedAt(currentDate)
                .setExpiration(expireDate)
                .signWith(key())
                .compact();

        return token;
    }

    private Key key() {
        return Keys.hmacShaKeyFor(Decoders.BASE64.decode(jwtSecret));
    }

    public String getUsername(String token) {
        Claims claims = Jwts.parserBuilder()
                .setSigningKey(key())
                .build()
                .parseClaimsJws(token)
                .getBody();

        return claims.getSubject();
    }

    public boolean validateToken(String token) {
        try {
            Jwts.parserBuilder()
                    .setSigningKey(key())
                    .build()
                    .parse(token);
            return true;
        } catch (MalformedJwtException e) {
            throw new APIException(TOKEN_INVALID, HttpStatus.BAD_REQUEST);
        } catch (ExpiredJwtException e) {
            throw new APIException(TOKEN_EXPIRED, HttpStatus.BAD_REQUEST);
        } catch (UnsupportedJwtException e) {
            throw new APIException(TOKEN_UNSUPPORTED, HttpStatus.BAD_REQUEST);
        } catch (IllegalArgumentException e) {
            throw new APIException(TOKEN_EMPTY, HttpStatus.BAD_REQUEST);
        }
    }
}
